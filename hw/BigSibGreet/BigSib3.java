public class BigSib3 {
    private String helloMsg;
    public BigSib3 (String derek) {
	helloMsg = derek;
    } // end BigSib3 ()
    public String greet (String after) {
	String ans = helloMsg + after;
	return ans;
    } // end greet ()
} // end BigSib3
    
